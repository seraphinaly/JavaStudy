
public class review{

	public static void main(String[] args){
	  //반복문
	  //1.for문
		for(int i=1; i<=5; i++){
			System.out.print(i+" ");
		}

		System.out.println();
	  //2.while문	
		int i=1;
		while(i<=5){
			System.out.print(i+" ");
			i++;
		}

		System.out.println();
	  //3.do~while문
		i=1;
		do{
			System.out.print(i+" ");
			i++;
		} while(i<=5);

	}

}
