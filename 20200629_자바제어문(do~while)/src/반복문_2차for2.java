public class 반복문_2차for2{

	public static void main(String[] args){
		for(int i=1;i<=5;i++) {
			System.out.println("**********");
		}

		System.out.println("\n==2차 for==\n");
		for(int i=1;i<=5;i++) {
			for(int j=1;j<=10;j++) {
				System.out.print("*");
		}
			System.out.println();
		}
		
		
		System.out.println();
		

			
		}
	}


